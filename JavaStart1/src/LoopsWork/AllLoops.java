package LoopsWork;

import java.util.Arrays;

public class AllLoops {

	public static void main(String[] args) {
		
		// loop for
		for (int i = 10; i <= 20; i++) {
			System.out.println(i);
		}
		System.out.println("---------------");
		
		// loop while
		int x = 10;
		while(x <= 20) {
			System.out.println(x);
			x++;
		}
		System.out.println("---------------");
		
		// loop do-while
		int y = 10;
		do{
			System.out.println(y);
			y++;
			
		}while (y < 21);
		System.out.println("---------------");
		
	

	}

}
